		<h1>O Consórcio</h1>	
		
		<h2>Consórcio Servopa desde 1966</h2>
		<p>Fruto da criatividade brasileira e instrumento fantástico na captação de poupança, o consórcio 
		surgiu no país no início dos anos 60, sendo gradativamente ajustado e normatizado afim de atender 
		as expectativas e necessidades do consumidor. A partir de 1991 o Banco Central do Brasil passou a 
		gerir o sistema de consórcio, adaptando-o ao atual Código de Defesa do Consumidor. Para o setor 
		automotivo, o sistema de consórcio já representou 30% do escoamento da produção nacional em 
		automóveis e utilitários leves, 60% da produção de motocicletas, 40% em caminhões e 20% em 
		tratores. Fundado em 1966, com sede em Curitiba, no Estado do Paraná, o Consórcio Nacional 
		Servopa atua em todas as regiões do país, através de empresas parceiras, nos segmentos de 
		automóveis, motos, caminhões, imóveis e tratores. Oferece uma ampla variedade de bens e marcas 
		para créditos de veículos novos ou semi-novos, com parcelas que se ajustam ao poder aquisitivo 
		de seus diversificados clientes.</p>
		
		<h2>Atuação</h2>

		<p>O Consórcio Nacional Servopa já entregou, desde sua fundação, mais de 170.000 cartas de crédito, 
		e vem se destacando por sua idoneidade, administração, segurança, e entre outros. Possui em média 
		250 grupos ativos com mais de 35.000 consorciados posicionando-se entre as dez maiores administradoras 
		do país (fonte Banco Central - www.bcb.gov.br - Fone: 0800-9792345). Estes resultados expressam a 
		organização e a seriedade de uma estrutura com mais de 150 profissionais treinados a prestar serviços 
		adequados, no tempo certo, apoiados por um moderno Call-Center para informações via telefone, internet 
		e visitas pessoais de vendedores credenciados aos clientes. Acrescenta-se ainda uma equipe de 
		supervisores de apoio às empresas e indústrias parceiras, que percorrem todos os Estados do Brasil, 
		acompanhando e oferecendo treinamento em vendas, materiais promocionais, explicações técnicas, 
		assembléias exclusivas locais, equipamentos de apoio, etc. Esta relação de reciprocidade vem 
		garantir o sucesso do Consórcio Nacional Servopa com concessionários das marcas General Motors, 
		Volkswagen, Fiat, Toyota, Chrysler, Audi, Honda, Renault, Peugeot, entre outras. E ainda, a 
		administração exclusiva dos grupos CNCV - Consórcio Nacional de Caminhões Volkswagen, nos 
		Estados do Mato Grosso do Sul, Rio de Janeiro, Paraná, Santa Catarina, Rio Grande do Sul.</p>
		
		
